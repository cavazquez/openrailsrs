const fn field(
    token: &'static str,
    aliases: &'static [&'static str],
    applies_to: VehicleKind,
    openrailsrs: ParserSupport,
    openbve: ParserSupport,
    category: &'static str,
    notes: &'static str,
) -> VehicleFieldSpec {
    VehicleFieldSpec {
        token,
        aliases,
        applies_to,
        openrailsrs,
        openbve,
        category,
        notes,
    }
}

/// Lookup catalog entry by token (case-insensitive), including aliases.
pub fn lookup_field(token: &str) -> Option<&'static VehicleFieldSpec> {
    let upper = token.to_ascii_uppercase();
    VEHICLE_FIELD_CATALOG.iter().find(|spec| {
        spec.token.eq_ignore_ascii_case(token)
            || spec.aliases.iter().any(|a| a.eq_ignore_ascii_case(token))
            || spec.token.to_ascii_uppercase() == upper
    })
}

/// All catalog entries applicable to the given vehicle kind.
pub fn catalog_for_kind(kind: VehicleKind) -> impl Iterator<Item = &'static VehicleFieldSpec> {
    VEHICLE_FIELD_CATALOG.iter().filter(move |spec| {
        spec.applies_to == VehicleKind::Both || spec.applies_to == kind || kind == VehicleKind::Both
    })
}
