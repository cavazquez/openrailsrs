//! Static catalog of MSTS `.eng` / `.wag` tokens for cross-reference audits.
//!
//! Sources: openrailsrs typed parsers, OpenBVE `VehicleParser.cs`, Open Rails
//! `EngineFile.cs` / `MSTSLocomotive.cs` (documented in `docs/FORMATS.md`).

/// Which vehicle files a catalog entry applies to.
#[derive(Clone, Copy, Debug, PartialEq, Eq)]
pub enum VehicleKind {
    Engine,
    Wagon,
    Both,
}

/// Whether a parser consumes a token for simulation or presentation.
#[derive(Clone, Copy, Debug, PartialEq, Eq, PartialOrd, Ord, Hash)]
pub enum ParserSupport {
    /// Fully mapped into openrailsrs train/sim types.
    Parsed,
    /// Token read but only partially used (defaults, subset of block).
    Partial,
    /// Known token intentionally skipped (visual/audio-only).
    Ignored,
    /// Not handled by this parser.
    NotImplemented,
    /// Does not apply to this parser (e.g. ORTS-only for OpenBVE).
    NotApplicable,
}

/// One row in the vehicle field catalog.
#[derive(Clone, Copy, Debug)]
pub struct VehicleFieldSpec {
    pub token: &'static str,
    pub aliases: &'static [&'static str],
    pub applies_to: VehicleKind,
    pub openrailsrs: ParserSupport,
    pub openbve: ParserSupport,
    pub category: &'static str,
    pub notes: &'static str,
}

