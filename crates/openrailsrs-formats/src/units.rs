pub fn lb_to_kg(value_lb: f64) -> f64 {
    value_lb * 0.453_592_37
}

pub fn kw_to_w(value_kw: f64) -> f64 {
    value_kw * 1_000.0
}

pub fn mph_to_mps(value_mph: f64) -> f64 {
    value_mph * 0.447_04
}

pub fn kn_to_n(value_kn: f64) -> f64 {
    value_kn * 1_000.0
}

pub fn kmh_to_mps(value_kmh: f64) -> f64 {
    value_kmh / 3.6
}
